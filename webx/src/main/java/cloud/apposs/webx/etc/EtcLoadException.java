package cloud.apposs.webx.etc;

public class EtcLoadException extends Exception {
	private static final long serialVersionUID = 7023543282841842720L;

	public EtcLoadException() {
		super();
	}

	public EtcLoadException(String message, Throwable cause) {
		super(message, cause);
	}

	public EtcLoadException(String message) {
		super(message);
	}

	public EtcLoadException(Throwable cause) {
		super(cause);
	}
}
